package org.logicware.db;

import java.util.Objects;

import org.logicware.jpi.PrologClause;

public final class Row {

	private final PrologClause clause;

	public Row(PrologClause clause) {
		this.clause = clause;
	}

	public PrologClause getClause() {
		return clause;
	}

	@Override
	public String toString() {
		return "" + clause + "";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((clause == null) ? 0 : clause.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Row other = (Row) obj;
		return Objects.equals(clause, other.clause);
	}

}
