package org.prolobjectlink.db;

public class DatabaseRole extends DatabaseGrantee {

	private final String name;
	private final boolean system;
	private static final long serialVersionUID = 8113599351922702097L;

	public DatabaseRole(String name, boolean system) {
		this.system = system;
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public boolean isSystem() {
		return system;
	}

	@Override
	public String toString() {
		return name;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + (system ? 1231 : 1237);
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		DatabaseRole other = (DatabaseRole) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return system == other.system;
	}

}
